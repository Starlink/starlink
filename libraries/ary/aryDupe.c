#include "sae_par.h"
#include "ary1.h"
#include "mers.h"
#include "ary_err.h"
#include "dat_par.h"
#include <string.h>

void aryDupe( Ary *ary1, AryPlace **place, Ary **ary2, int *status ) {
/*
*+
*  Name:
*     aryDupe

*  Purpose:
*     Duplicate an array.

*  Synopsis:
*     void aryDupe( Ary *ary1, AryPlace **place, Ary **ary2, int *status )

*  Description:
*     This function duplicates an array, creating a new base array with
*     the same attributes as an existing array (or array section). The
*     new array is left in an undefined state.

*  Parameters:
*     ary1
*        Identifier for the array to be duplicated.
*     place
*        An array placeholder (e.g. generated by the aryPlace routine)
*        which indicates the position in the data system where the new
*        array will reside. The placeholder is annulled by this
*        routine, and a value of NULL will be returned.
*     ary2
*        Returned holding the identifier for the new duplicate array.
*     status
*        The global status.

*  Notes:
*     -  Duplicating a scaled or delta array produces an equivalent
*     simple array.
*     -  If this routine is called with "status" set, then a value of
*     NULL will be returned for the "ary" argument, although no
*     further processing will occur. The same value will also be
*     returned if the function should fail for any reason.  In either
*     event, the placeholder will still be annulled.

*  Copyright:
*      Copyright (C) 2017 East Asian Observatory
*      All rights reserved.

*  Licence:
*     This program is free software; you can redistribute it and/or
*     modify it under the terms of the GNU General Public License as
*     published by the Free Software Foundation; either version 2 of
*     the License, or (at your option) any later version.
*
*     This program is distributed in the hope that it will be
*     useful,but WITHOUT ANY WARRANTY; without even the implied
*     warranty of MERCHANTABILITY or FITNESS FOR A PARTICULAR
*     PURPOSE. See the GNU General Public License for more details.
*
*     You should have received a copy of the GNU General Public License
*     along with this program; if not, write to the Free Software
*     Foundation, Inc., 51 Franklin Street,Fifth Floor, Boston, MA
*     02110-1301, USA

*  Authors:
*     RFWS: R.F. Warren-Smith (STARLINK)
*     DSB: David S. Berry (EAO)

*  History:
*     03-JUL-2017 (DSB):
*        Original version, based on equivalent Fortran routine by RFWS.

*-
*/

/* Local variables: */
   AryACB *acb1;              /* Input array ACB */
   AryACB *acb2;              /* Output array ACB */
   AryDCB *dcb1;              /* Input data object DCB */
   AryDCB *dcb2=NULL;         /* Output data object DCB */
   AryPCB *pcb;               /* Placeholder entry in the PCB */
   char newtyp[DAT__SZTYP+1]; /* Data type for new array */
   int erase;                 /* Whether to erase placeholder object */
   int pbnd;                  /* Whether array bounds are primitive */
   int tstat;                 /* Temporary status variable */

/* Set an initial value for the "ary2" argument. */
   *ary2 = NULL;

/* Save the STATUS value and mark the error stack. */
   tstat = *status;
   errMark();

/* Import the array placeholder, converting it to a PCB pointer. */
   *status = SAI__OK;
   pcb = (AryPCB *) ary1Impid( *place, 0, 0, 0, status );

/* If there has been no error at all so far, then import the input array
   identifier. */
   acb2 = NULL;
   if( ( *status == SAI__OK ) && ( tstat == SAI__OK ) ){
      acb1 = (AryACB *) ary1Impid( ary1, 1, 1, 1, status );
      if( *status == SAI__OK ){
         ARY__DCB_LOCK_MUTEX;

/* Obtain an index to the input data object entry in the DCB and ensure
   that storage form information is available for it. */
         dcb1 = acb1->dcb;
         ary1Dfrm( dcb1, status );

/* Handle each form of array in turn... */
         if( *status == SAI__OK ){

/* Primitive arrays.
   ================ */
            if( !strcmp( dcb1->form, "PRIMITIVE" ) ){

/* Ensure that data type and bounds information is available for the data
   object. */
               ary1Dtyp( dcb1, status );
               ary1Dbnd( dcb1, status );

/* See if the array's bounds are suitable for creating a new primitive
   array. */
               ary1Pbnd( acb1, &pbnd, status );
               if( *status == SAI__OK ){

/* Create a new data object with the same attributes and an entry in the
   DCB. Create a primitive array if possible. Otherwise create a simple
   array. */
                  if( pbnd ){
                     ary1Dcrep( 1, dcb1->type, acb1->ndim, acb1->ubnd,
                                pcb->tmp, &pcb->loc, &dcb2, status );
                  } else {
                     ary1Dcre( 1, dcb1->type, dcb1->complex, acb1->ndim,
                               acb1->lbnd, acb1->ubnd, pcb->tmp, pcb->loc,
                               &dcb2, status );
                  }
               }

/* Simple, delta and scaled arrays.
   ================================ */
            } else if( !strcmp( dcb1->form, "SIMPLE" ) ||
                       !strcmp( dcb1->form, "SCALED" ) ||
                       !strcmp( dcb1->form, "DELTA" ) ){

/* Ensure that data type and bounds information is available for the data
   object. */
               ary1Dtyp( dcb1, status );
               ary1Dbnd( dcb1, status );

/* Get the required data type for the new array. */
               ary1Extyp( dcb1, newtyp, status );

/* Create a new data object with the same attributes and an entry in the
   DCB. This is a simple array. The act of duplicating a scaled or delta
   array creates a simple array. */
               ary1Dcre( 1, newtyp, dcb1->complex, acb1->ndim, acb1->lbnd,
                         acb1->ubnd, pcb->tmp, pcb->loc, &dcb2, status );

/* If the DCB form entry was not recognised, then report an error. */
            } else {
               *status = ARY__FATIN;
               msgSetc( "BADFORM", dcb1->form );
               errRep( " ", "Invalid array form '^BADFORM' found in Data "
                       "Control Block (internal programming error).", status );
            }
         }

/* Create a base array entry in the ACB to describe the new data object. */
         ary1Crnba( dcb2, &acb2, status );

/* Export an identifier for the new array. */
         *ary2 = ary1Expid( (AryObject *) acb2, status );

/* If an error has occurred, then annul the new ACB entry. */
         if( ( *status != SAI__OK ) && acb2 ){
            ARY__ACB_LOCK_MUTEX;
            acb2 = ary1Anl( acb2, status );
            ARY__ACB_UNLOCK_MUTEX;
         }

         ARY__DCB_UNLOCK_MUTEX;
      }
   }

/* Annul the placeholder, erasing the associated object if any error has
   occurred. */
   if( pcb ){
      erase = ( *status != SAI__OK ) || ( tstat != SAI__OK );
      ary1Annpl( erase, &pcb, status );
   }

/* Reset the PLACE argument. */
   *place = NULL;

/* Annul any error if STATUS was previously bad, otherwise let the new
   error report stand. */
   if( *status != SAI__OK ){
      if( tstat != SAI__OK ){
         errAnnul( status );
         *status = tstat;

/* Report context information and call the error tracing routine if
   appropriate. */
      } else {
         errRep( " ", "aryDupe: Error duplicating an array.", status );
         ary1Trace( "aryDupe", status );
      }
   } else {
      *status = tstat;
   }

/* Release the error stack. */
   errRlse();

}
