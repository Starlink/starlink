            DO 3 I = EL, 1, -1

*  Evaluate the quality masking function.
               IF ( .NOT. NDF_QMASK( QUAL( I ), BADBIT ) ) THEN

*  Set affected array elements to the bad value and note this has
*  happened.
                  ARRAY1( I ) = VAL__BADB
                  ARRAY2( I ) = VAL__BADB
                  ARRAY3( I ) = VAL__BADB
                  BAD = .TRUE.
               END IF
3           CONTINUE

*  4 arrays to process:
*  ===================
*  Loop to process each array element.
         ELSE IF ( NARRAY .EQ. 4 ) THEN
            DO 4 I = EL, 1, -1

*  Evaluate the quality masking function.
               IF ( .NOT. NDF_QMASK( QUAL( I ), BADBIT ) ) THEN

